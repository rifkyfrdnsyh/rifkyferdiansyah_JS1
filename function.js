function pertambahan(a, b) {
  return a + b;
}

let hasilPertambahan = pertambahan(6, 6);

function pengurangan(a, b) {
  return a - b;
}

let hasilPengurangan = pengurangan(9, 2);

function pembagian(a, b) {
  return a / b;
}

let hasilPembagian = pembagian(12, 6);

console.log(`hasil = ${hasilPertambahan}`);
console.log(`hasil = ${hasilPengurangan}`);
console.log(`hasil = ${hasilPembagian}`);
